class unboxing{
    public static void main(String[] args){
        Integer a = 255;
        Float b = 56.7F;
        Character c = 'h';

        //Unboxing

        int x = a;
        float y = b;
        char z = c;

        System.out.println(x);
        System.out.println(y);
        System.out.println(z);
    }
}